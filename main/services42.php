<p>При модернизации в первую очередь владелец ПК должен сам установить пределы повышения производительности: максимальные объемы жесткого диска, оперативной памяти, желаемая производительность процессора и видеокарты. Это несложно выяснять, разобравшись, какие Вы задачи собираетесь выполнять на Вашем ПК, будете ли играть в игры и т.п.</p>
