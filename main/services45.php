<li>Замена или ремонт аккумулятора;</li>
<li>Замена различных элементов корпуса;</li>
<li>Ремонт цепи зарядки навигатора;</li>
<li>Замена или ремонт разъёмов навигатора;</li>
<li>Замена или ремонт кнопок навигатора;</li>
<li>Ремонт системной платы, ремонт цепей питания системной платы;</li>
<li>Ремонт если навигатор не видит спутники (программный ремонт или ремонт GPS модуля).</li>
</ol>
